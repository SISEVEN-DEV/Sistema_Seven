            if (mtxtpData.Text == "" || mtxtpData1.Text == "")
            {
                MessageBox.Show("Existem campos obrigatórios que precisam ser preenchidos:\n< Data de Inicio e Fim >.", "Atenção", MessageBoxButtons.OK, MessageBoxIcon.Exclamation);
                this.DialogResult = DialogResult.None;
                mtxtpData.Select();
                //
                mtxtpData.TextMaskFormat = MaskFormat.IncludePromptAndLiterals;
                mtxtpData1.TextMaskFormat = MaskFormat.IncludePromptAndLiterals;
            }
            else
            {
                mtxtpData.TextMaskFormat = MaskFormat.IncludePromptAndLiterals;
                mtxtpData1.TextMaskFormat = MaskFormat.IncludePromptAndLiterals;
